package abstractclass;

abstract public class Product {

	public void Price() {
		System.out.println("Price of the book 10");
	}
	
	public void review() {
		System.out.println("4 star rating");
	}
	
	public void books() {
		System.out.println("Harry Potter");
	}
	
	abstract public void date ();
	
	
}
